void DISP_DrawCharFont1612(uint16_t x, uint16_t y, uint8_t ascii, DISP_FontMode_t fntMode)
{
    uint8_t i, j, y0 = y;
    uint8_t tmpChar = 0;
    uint8_t ch = 0;


    if (ascii < '0' | ascii > ':')
    	return;

    ch = ascii - '0';
    for(i = 0; i < DISP_F1612_BYTES_PER_FONT; i++)
    {
    	tmpChar = pgm_read_byte(&Font1612Glyphs[ch][i]);
		for(j = 0; j < 8; j++)  // scanning all bits in a current byte
        {
            if(tmpChar & 0x80) SH1106_setPixel(x, y, 1);	//todo: add mode: positive, negative
            else SH1106_setPixel(x, y, 0);

            tmpChar <<= 1;
            y++;
            if((y - y0) == Font1612.height)
            {
                y = y0;
                x++;
                break;  // if height of the glyph is bigger than by 8,
                        // skip the rest of bits in the current byte, and load next one
            }
        }
    }

}

